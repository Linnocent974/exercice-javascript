const mention = 'A';

switch(mention){
    case 'A' :
        console.log('Vous avez reussi la mention A');
        break;
    case 'B' :
        console.log('Vous avez reussi la mention B');
        break;
    case 'C' :
        console.log('Vous avez reussi la mention C');
        break;
    case 'E' :
        console.log('Vous avez reussi la mention E');
        break;
    default :
    console.log("Cette mention n'est pas disponible");
}